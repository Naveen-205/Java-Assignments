package first.index;

public class Datatypes {
	public static void main (String[] args) {
		int i = 10; //4bytes
		byte b = 20; // 1byte
		short s = 300; // 2bytes
		long l = 2L; //8bytes
		float f =  12.5f; //4bytes
		double d = 99.99; // 8bytes
		char c = 'N'; // 2bytes
		boolean fun = true; // 1bit logically
		System.out.println("This is Integer:" +i);
		System.out.println("This is Byte:" +b);
		System.out.println("This is short:" +s);
		System.out.println("This is long:" +l);
		System.out.println("This is float:" +f);
		System.out.println("This is double:" +d);
		System.out.println("This is char:" +c);
		System.out.println("This is boolean:" +fun);
		}
}                       
