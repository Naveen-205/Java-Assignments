package first.index;

public class Salary {
	public static void main(String[] args) {
		
		int salary = 15000;
		System.out.println("Your Salary: " + salary);
		
		salary += 5000;
		System.out.println("After Increment: " + salary);
		
		salary -= 1500;
		System.out.println("After Decrement: " + salary);
		
		salary *= 2;
		System.out.println("After Multiplication: " + salary);
		
		salary /= 2;
		System.out.println("After Division: " + salary);
	}
}
